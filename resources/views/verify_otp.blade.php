<script>
    $(function () {
      
        $('#sub').click(function () {
            var countryCode = $('#countryCode').val();
            var firm = $('#number').val();
           var num=countryCode+firm;
          

            var _token = $('#csrf-token').val();

            if (countryCode != '' && firm!='' ) {
                $.ajax({
                    url: '/num',
                    type: 'post',
                    data: {num: num, _token: _token},
                    success: function (response)
                    {
                       // alert(response);
                       //console.log();
                       if(response=="Please Enter Otp!"){
                        $('#otp').show();
                        $("#num").hide();
                       $('#mas').html("<div class='alert alert-danger'>" + response + "</div>");


                     }
                     else{
                       $('#ms').html("<div class='alert alert-danger'>" + response + "</div>");
                     }
                    }
                });
            }
            else{
              $('#mas').html("<div class='alert alert-danger'>Please Enter Creact Number</div>");
            }
        });
    });
</script>
